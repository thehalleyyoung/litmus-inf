/// An inner automorphism: conjugation by a group element.
///
/// For a group element g, the inner automorphism φ_g is defined by
/// φ_g(x) = g x g^{-1}.
#[derive(Debug, Clone)]
pub struct InnerAutomorphism {
    /// Index of the conjugating element.
    pub conjugator: usize,
    /// The resulting automorphism.
    pub automorphism: Automorphism,
}

impl InnerAutomorphism {
    /// Create an inner automorphism by conjugation with element g.
    pub fn new(group: &FiniteGroup, g: usize) -> Self {
        let n = group.order();
        let g_inv = group.inverse_of(g);
        let mapping: Vec<usize> = (0..n)
            .map(|x| group.multiply(group.multiply(g, x), g_inv))
            .collect();
        Self {
            conjugator: g,
            automorphism: Automorphism::new(mapping),
        }
    }

    /// Get the automorphism.
    pub fn as_automorphism(&self) -> &Automorphism { &self.automorphism }

    /// Apply the inner automorphism to an element.
    pub fn apply(&self, x: usize) -> usize {
        self.automorphism.apply(x)
    }

    /// Conjugate an element: g x g^{-1}.
    pub fn conjugate(&self, group: &FiniteGroup, x: usize) -> usize {
        let g = self.conjugator;
        let g_inv = group.inverse_of(g);
        group.multiply(group.multiply(g, x), g_inv)
    }

    /// Compute all inner automorphisms of a group.
    pub fn all_inner_automorphisms(group: &FiniteGroup) -> Vec<InnerAutomorphism> {
        let n = group.order();
        let mut seen = HashSet::new();
        let mut result = Vec::new();
        for g in 0..n {
            let inner = InnerAutomorphism::new(group, g);
            if seen.insert(inner.automorphism.mapping.clone()) {
                result.push(inner);
            }
        }
        result
    }
}

/// A conjugacy class in a group.
#[derive(Debug, Clone)]
pub struct ConjugacyClass {
    /// Representative element index.
    pub representative: usize,
    /// All elements in the class.
    pub elements: Vec<usize>,
    /// Size of the centralizer of the representative.
    pub centralizer_order: usize,
}

impl ConjugacyClass {
    /// Size of the class.
    pub fn size(&self) -> usize { self.elements.len() }
}

impl fmt::Display for ConjugacyClass {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Class(rep={}, size={}, |C|={})",
            self.representative, self.elements.len(), self.centralizer_order)
    }
}

