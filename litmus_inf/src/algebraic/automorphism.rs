/// The automorphism tower of a group: G → Aut(G) → Aut(Aut(G)) → ...
#[derive(Debug, Clone)]
pub struct AutomorphismTower {
    /// Orders of the groups in the tower.
    pub tower_orders: Vec<usize>,
    /// Whether the tower stabilizes.
    pub stabilized: bool,
    /// Height at which stabilization occurs.
    pub stable_height: Option<usize>,
}

impl AutomorphismTower {
    /// Compute the automorphism tower up to a maximum height.
    pub fn compute(group: &FiniteGroup, max_height: usize) -> Self {
        let mut tower_orders = vec![group.order()];
        let mut current_group = group.clone();
        for h in 0..max_height {
            let aut_group = AutomorphismGroup::compute(&current_group);
            tower_orders.push(aut_group.order);
            // Check stabilization
            if aut_group.order == current_group.order() {
                return Self {
                    tower_orders,
                    stabilized: true,
                    stable_height: Some(h + 1),
                };
            }
            // Build the next group from automorphisms
            if aut_group.order > 20 { break; }
            // Convert automorphisms to permutations for the next level
            let degree = aut_group.order;
            let perms: Vec<Permutation> = aut_group.automorphisms.iter().enumerate().map(|(i, _)| {
                let images: Vec<u32> = (0..degree).map(|j| {
                    // Cayley representation: left multiplication
                    let composed = aut_group.automorphisms[i].compose(&aut_group.automorphisms[j]);
                    aut_group.automorphisms.iter().position(|a| a.mapping == composed.mapping)
                        .unwrap_or(0) as u32
                }).collect();
                Permutation::new(images)
            }).collect();
            current_group = FiniteGroup::new(perms);
        }
        Self {
            tower_orders,
            stabilized: false,
            stable_height: None,
        }
    }

    /// Height of the tower (number of levels computed).
    pub fn height(&self) -> usize { self.tower_orders.len() }

    /// Check if the tower stabilizes (group is complete).
    pub fn is_complete(&self) -> bool { self.stabilized }
}

