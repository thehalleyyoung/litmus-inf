/// Detector and verifier for characteristic subgroups.
#[derive(Debug, Clone)]
pub struct CharacteristicSubgroupFinder {
    /// The group.
    group: FiniteGroup,
    /// All automorphisms.
    automorphisms: Vec<Automorphism>,
}

impl CharacteristicSubgroupFinder {
    /// Create a new finder.
    pub fn new(group: FiniteGroup, automorphisms: Vec<Automorphism>) -> Self {
        Self { group, automorphisms }
    }

    /// Check if a subgroup is characteristic (fixed by all automorphisms).
    pub fn is_characteristic(&self, subgroup: &[usize]) -> bool {
        let h_set: HashSet<usize> = subgroup.iter().copied().collect();
        self.automorphisms.iter().all(|aut| {
            subgroup.iter().all(|&h| h_set.contains(&aut.apply(h)))
        })
    }

    /// Find all characteristic subgroups.
    pub fn find_characteristic_subgroups(&self) -> Vec<Vec<usize>> {
        let n = self.group.order();
        let mut result = Vec::new();
        // Try all subsets that form subgroups
        // For small groups, use brute force
        if n <= 16 {
            for mask in 0..(1u64 << n) {
                let subset: Vec<usize> = (0..n).filter(|&i| mask & (1u64 << i) != 0).collect();
                if self.group.is_subgroup(&subset) && self.is_characteristic(&subset) {
                    result.push(subset);
                }
            }
        } else {
            // For larger groups, check known characteristic subgroups
            let center = self.group.center();
            if self.is_characteristic(&center) { result.push(center); }
            let comm = self.group.commutator_subgroup();
            if self.is_characteristic(&comm) { result.push(comm); }
        }
        result
    }

    /// Get the center (always characteristic).
    pub fn center(&self) -> Vec<usize> { self.group.center() }

    /// Get the commutator subgroup (always characteristic).
    pub fn commutator_subgroup(&self) -> Vec<usize> { self.group.commutator_subgroup() }

    /// Compute the Frattini subgroup: intersection of all maximal subgroups.
    pub fn frattini_subgroup(&self) -> Vec<usize> {
        let n = self.group.order();
        let maximal = self.find_maximal_subgroups();
        if maximal.is_empty() {
            return (0..n).collect(); // entire group
        }
        let mut intersection: HashSet<usize> = maximal[0].iter().copied().collect();
        for sub in &maximal[1..] {
            let sub_set: HashSet<usize> = sub.iter().copied().collect();
            intersection = intersection.intersection(&sub_set).copied().collect();
        }
        let mut result: Vec<usize> = intersection.into_iter().collect();
        result.sort();
        result
    }

    /// Find maximal subgroups.
    fn find_maximal_subgroups(&self) -> Vec<Vec<usize>> {
        let n = self.group.order();
        let mut subgroups = Vec::new();
        if n <= 16 {
            for mask in 1..(1u64 << n) - 1 {
                let subset: Vec<usize> = (0..n).filter(|&i| mask & (1u64 << i) != 0).collect();
                if subset.len() < n && self.group.is_subgroup(&subset) {
                    subgroups.push(subset);
                }
            }
        }
        // Filter to maximal ones
        let mut maximal = Vec::new();
        for (i, sub) in subgroups.iter().enumerate() {
            let sub_set: HashSet<usize> = sub.iter().copied().collect();
            let is_maximal = !subgroups.iter().enumerate().any(|(j, other)| {
                j != i && other.len() > sub.len() && sub.iter().all(|x| other.contains(x))
            });
            if is_maximal { maximal.push(sub.clone()); }
        }
        maximal
    }
}

