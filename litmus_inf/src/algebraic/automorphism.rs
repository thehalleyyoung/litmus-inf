/// The outer automorphism group Out(G) = Aut(G) / Inn(G).
#[derive(Debug, Clone)]
pub struct OuterAutomorphismGroup {
    /// Coset representatives of Inn(G) in Aut(G).
    pub coset_representatives: Vec<Automorphism>,
    /// Order of Out(G).
    pub order: usize,
}

impl OuterAutomorphismGroup {
    /// Compute Out(G) from the full automorphism group and inner automorphisms.
    pub fn compute(all_auts: &[Automorphism], inner_auts: &[Automorphism]) -> Self {
        let inner_set: HashSet<Vec<usize>> = inner_auts.iter()
            .map(|a| a.mapping.clone())
            .collect();
        let mut coset_reps = Vec::new();
        let mut seen_cosets: HashSet<Vec<usize>> = HashSet::new();
        for aut in all_auts {
            // Compute the coset aut * Inn(G) and check if we've seen it
            let mut coset_key = Vec::new();
            for inner in inner_auts {
                let composed = aut.compose(inner);
                let mut key = composed.mapping.clone();
                key.sort();
                coset_key.extend(key);
            }
            if seen_cosets.insert(coset_key) {
                coset_reps.push(aut.clone());
            }
        }
        let order = coset_reps.len();
        Self { coset_representatives: coset_reps, order }
    }

    /// Check if an automorphism is outer (not inner).
    pub fn is_outer(aut: &Automorphism, inner_auts: &[Automorphism]) -> bool {
        !inner_auts.iter().any(|inner| inner.mapping == aut.mapping)
    }

    /// Get the order of Out(G).
    pub fn outer_order(&self) -> usize { self.order }
}

