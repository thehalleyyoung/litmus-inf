/// Engine for conjugation operations and conjugacy-related computations.
#[derive(Debug, Clone)]
pub struct ConjugationEngine {
    /// Reference to the group.
    group_elements: Vec<Permutation>,
    /// Cayley table for fast multiplication.
    cayley: Vec<Vec<usize>>,
    /// Inverse table.
    inverses: Vec<usize>,
}

impl ConjugationEngine {
    /// Create from a FiniteGroup.
    pub fn from_group(group: &FiniteGroup) -> Self {
        Self {
            group_elements: group.elements.clone(),
            cayley: group.cayley.clone(),
            inverses: group.inverses.clone(),
        }
    }

    /// Conjugate x by g: g x g^{-1}.
    pub fn conjugate(&self, g: usize, x: usize) -> usize {
        self.cayley[self.cayley[g][x]][self.inverses[g]]
    }

    /// Check if two elements are conjugate.
    pub fn are_conjugate(&self, a: usize, b: usize) -> bool {
        let n = self.group_elements.len();
        (0..n).any(|g| self.conjugate(g, a) == b)
    }

    /// Compute all conjugacy classes.
    pub fn conjugacy_classes(&self) -> Vec<ConjugacyClass> {
        let n = self.group_elements.len();
        let mut assigned = vec![false; n];
        let mut classes = Vec::new();
        for rep in 0..n {
            if assigned[rep] { continue; }
            let mut class = Vec::new();
            for g in 0..n {
                let conj = self.conjugate(g, rep);
                if !assigned[conj] {
                    assigned[conj] = true;
                    class.push(conj);
                }
            }
            class.sort();
            class.dedup();
            let centralizer_order = n / class.len();
            classes.push(ConjugacyClass {
                representative: rep,
                elements: class,
                centralizer_order,
            });
        }
        classes
    }

    /// Compute the class equation: |G| = |Z(G)| + Σ [G : C_G(x_i)].
    pub fn class_equation(&self) -> Vec<usize> {
        self.conjugacy_classes().iter().map(|c| c.size()).collect()
    }

    /// Compute the centralizer of an element: C_G(x) = { g | gx = xg }.
    pub fn centralizer(&self, x: usize) -> Vec<usize> {
        let n = self.group_elements.len();
        (0..n).filter(|&g| self.cayley[g][x] == self.cayley[x][g]).collect()
    }

    /// Compute the normalizer of a subgroup H: N_G(H) = { g | gHg^{-1} = H }.
    pub fn normalizer(&self, subgroup: &[usize]) -> Vec<usize> {
        let n = self.group_elements.len();
        let h_set: HashSet<usize> = subgroup.iter().copied().collect();
        (0..n).filter(|&g| {
            subgroup.iter().all(|&h| {
                let conj = self.conjugate(g, h);
                h_set.contains(&conj)
            })
        }).collect()
    }

    /// Compute the conjugation action as a permutation representation.
    pub fn conjugation_action(&self, g: usize) -> Vec<usize> {
        let n = self.group_elements.len();
        (0..n).map(|x| self.conjugate(g, x)).collect()
    }
}

