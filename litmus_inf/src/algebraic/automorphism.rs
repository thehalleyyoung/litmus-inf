/// An automorphism of a finite group: a bijective homomorphism G → G.
#[derive(Debug, Clone)]
pub struct Automorphism {
    /// Mapping: image[i] = index of φ(elements[i]).
    mapping: Vec<usize>,
    /// Inverse mapping.
    inverse_mapping: Vec<usize>,
}

impl Automorphism {
    /// Create a new automorphism from a mapping.
    pub fn new(mapping: Vec<usize>) -> Self {
        let n = mapping.len();
        let mut inverse_mapping = vec![0; n];
        for (i, &j) in mapping.iter().enumerate() {
            inverse_mapping[j] = i;
        }
        Self { mapping, inverse_mapping }
    }

    /// Create the identity automorphism.
    pub fn identity(n: usize) -> Self {
        Self::new((0..n).collect())
    }

    /// Apply the automorphism to an element index.
    pub fn apply(&self, i: usize) -> usize {
        self.mapping[i]
    }

    /// Get the preimage of an element index.
    pub fn preimage(&self, i: usize) -> usize {
        self.inverse_mapping[i]
    }

    /// Compose two automorphisms: (self ∘ other)(x) = self(other(x)).
    pub fn compose(&self, other: &Automorphism) -> Self {
        let n = self.mapping.len();
        let mapping: Vec<usize> = (0..n).map(|i| self.apply(other.apply(i))).collect();
        Self::new(mapping)
    }

    /// Compute the inverse automorphism.
    pub fn inverse(&self) -> Self {
        Self::new(self.inverse_mapping.clone())
    }

    /// Check if this is the identity automorphism.
    pub fn is_identity(&self) -> bool {
        self.mapping.iter().enumerate().all(|(i, &j)| i == j)
    }

    /// Verify that this is a valid automorphism of the group.
    pub fn verify_homomorphism(&self, group: &FiniteGroup) -> bool {
        let n = group.order();
        if self.mapping.len() != n { return false; }
        // Check bijection
        let mut seen = vec![false; n];
        for &j in &self.mapping {
            if j >= n || seen[j] { return false; }
            seen[j] = true;
        }
        // Check homomorphism: φ(ab) = φ(a)φ(b)
        for a in 0..n {
            for b in 0..n {
                let ab = group.multiply(a, b);
                let phi_ab = self.apply(ab);
                let phi_a_phi_b = group.multiply(self.apply(a), self.apply(b));
                if phi_ab != phi_a_phi_b { return false; }
            }
        }
        true
    }

    /// Fixed points of the automorphism.
    pub fn fixed_points(&self) -> Vec<usize> {
        self.mapping.iter().enumerate()
            .filter(|&(i, &j)| i == j)
            .map(|(i, _)| i)
            .collect()
    }

    /// Compute the order of this automorphism.
    pub fn order(&self) -> u64 {
        let id = Self::identity(self.mapping.len());
        let mut current = self.clone();
        let mut ord = 1u64;
        loop {
            if current.is_identity() { return ord; }
            current = current.compose(self);
            ord += 1;
            if ord > self.mapping.len() as u64 * 10 { return ord; }
        }
    }

    /// Get the image as a vector.
    pub fn image(&self) -> &[usize] { &self.mapping }
}

impl PartialEq for Automorphism {
    fn eq(&self, other: &Self) -> bool { self.mapping == other.mapping }
}
impl Eq for Automorphism {}

impl std::hash::Hash for Automorphism {
    fn hash<H: std::hash::Hasher>(&self, state: &mut H) {
        self.mapping.hash(state);
    }
}

impl fmt::Display for Automorphism {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Aut(")?;
        for (i, &j) in self.mapping.iter().enumerate() {
            if i > 0 { write!(f, ",")?; }
            write!(f, "{}→{}", i, j)?;
        }
        write!(f, ")")
    }
}

