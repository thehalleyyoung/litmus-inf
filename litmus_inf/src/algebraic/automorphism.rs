/// A finite group represented by its elements and Cayley table.
#[derive(Debug, Clone)]
pub struct FiniteGroup {
    /// Elements of the group (as permutations).
    pub elements: Vec<Permutation>,
    /// Cayley table: cayley[i][j] = index of elements[i] * elements[j].
    cayley: Vec<Vec<usize>>,
    /// Index of the identity element.
    identity_idx: usize,
    /// Inverse table: inverses[i] = index of elements[i]^{-1}.
    inverses: Vec<usize>,
    /// Element lookup: maps a permutation to its index.
    element_index: HashMap<Vec<u32>, usize>,
}

impl FiniteGroup {
    /// Create a group from a list of elements.
    pub fn new(elements: Vec<Permutation>) -> Self {
        let n = elements.len();
        let mut element_index = HashMap::new();
        for (i, e) in elements.iter().enumerate() {
            element_index.insert(e.images().to_vec(), i);
        }
        // Build Cayley table
        let mut cayley = vec![vec![0; n]; n];
        for i in 0..n {
            for j in 0..n {
                let product = elements[i].compose(&elements[j]);
                let idx = element_index.get(product.images()).copied().unwrap_or(0);
                cayley[i][j] = idx;
            }
        }
        // Find identity
        let degree = if elements.is_empty() { 0 } else { elements[0].degree() };
        let id = Permutation::identity(degree);
        let identity_idx = element_index.get(&id.images().to_vec()).copied().unwrap_or(0);
        // Build inverse table
        let mut inverses = vec![0; n];
        for i in 0..n {
            let inv = elements[i].inverse();
            inverses[i] = element_index.get(&inv.images().to_vec()).copied().unwrap_or(0);
        }
        Self { elements, cayley, identity_idx, inverses, element_index }
    }

    /// Create from generators by enumerating all products.
    pub fn from_generators(generators: &[Permutation], degree: usize) -> Self {
        let elements: Vec<Permutation> = enumerate_from_generators(generators, degree).into_iter().collect();
        Self::new(elements)
    }

    /// Order of the group.
    pub fn order(&self) -> usize { self.elements.len() }

    /// Get the identity element.
    pub fn identity(&self) -> &Permutation { &self.elements[self.identity_idx] }

    /// Multiply two elements (by index).
    pub fn multiply(&self, i: usize, j: usize) -> usize { self.cayley[i][j] }

    /// Get the inverse of an element (by index).
    pub fn inverse_of(&self, i: usize) -> usize { self.inverses[i] }

    /// Get all elements.
    pub fn get_elements(&self) -> &[Permutation] { &self.elements }

    /// Check if a permutation is in the group.
    pub fn contains(&self, p: &Permutation) -> bool {
        self.element_index.contains_key(&p.images().to_vec())
    }

    /// Get the index of a permutation.
    pub fn index_of(&self, p: &Permutation) -> Option<usize> {
        self.element_index.get(&p.images().to_vec()).copied()
    }

    /// Compute the center Z(G) = { g ∈ G | ∀h. gh = hg }.
    pub fn center(&self) -> Vec<usize> {
        let n = self.order();
        (0..n).filter(|&g| {
            (0..n).all(|h| self.cayley[g][h] == self.cayley[h][g])
        }).collect()
    }

    /// Check if the group is abelian.
    pub fn is_abelian(&self) -> bool {
        self.center().len() == self.order()
    }

    /// Compute a subgroup generated by given element indices.
    pub fn subgroup_generated_by(&self, generators: &[usize]) -> Vec<usize> {
        let mut subgroup: HashSet<usize> = HashSet::new();
        let mut queue: VecDeque<usize> = VecDeque::new();
        subgroup.insert(self.identity_idx);
        queue.push_back(self.identity_idx);
        for &g in generators {
            if subgroup.insert(g) { queue.push_back(g); }
        }
        while let Some(elem) = queue.pop_front() {
            for &gen in generators {
                let product = self.cayley[elem][gen];
                if subgroup.insert(product) { queue.push_back(product); }
                let product_inv = self.cayley[elem][self.inverses[gen]];
                if subgroup.insert(product_inv) { queue.push_back(product_inv); }
            }
        }
        let mut result: Vec<usize> = subgroup.into_iter().collect();
        result.sort();
        result
    }

    /// Check if a set of indices forms a subgroup.
    pub fn is_subgroup(&self, indices: &[usize]) -> bool {
        let set: HashSet<usize> = indices.iter().copied().collect();
        if !set.contains(&self.identity_idx) { return false; }
        for &i in indices {
            if !set.contains(&self.inverses[i]) { return false; }
            for &j in indices {
                if !set.contains(&self.cayley[i][j]) { return false; }
            }
        }
        true
    }

    /// Check if a subgroup is normal: ∀g∈G, ∀h∈H: ghg⁻¹ ∈ H.
    pub fn is_normal_subgroup(&self, subgroup: &[usize]) -> bool {
        let h_set: HashSet<usize> = subgroup.iter().copied().collect();
        let n = self.order();
        for g in 0..n {
            for &h in subgroup {
                let conj = self.cayley[self.cayley[g][h]][self.inverses[g]];
                if !h_set.contains(&conj) { return false; }
            }
        }
        true
    }

    /// Compute the commutator subgroup [G, G].
    pub fn commutator_subgroup(&self) -> Vec<usize> {
        let n = self.order();
        let mut commutators = HashSet::new();
        for g in 0..n {
            for h in 0..n {
                // [g, h] = g h g^{-1} h^{-1}
                let gh = self.cayley[g][h];
                let g_inv_h_inv = self.cayley[self.inverses[g]][self.inverses[h]];
                let comm = self.cayley[gh][g_inv_h_inv];
                commutators.insert(comm);
            }
        }
        let gens: Vec<usize> = commutators.into_iter().collect();
        self.subgroup_generated_by(&gens)
    }
}

impl fmt::Display for FiniteGroup {
    fn fmt(&self, f: &mut fmt::Formatter<'_>) -> fmt::Result {
        write!(f, "Group(order={})", self.order())
    }
}

