/// The full automorphism group Aut(G) of a finite group G.
#[derive(Debug, Clone)]
pub struct AutomorphismGroup {
    /// All automorphisms.
    pub automorphisms: Vec<Automorphism>,
    /// Order of Aut(G).
    pub order: usize,
    /// Inner automorphism subgroup (as indices into automorphisms).
    pub inner_indices: Vec<usize>,
}

impl AutomorphismGroup {
    /// Compute the full automorphism group of a finite group.
    ///
    /// Uses brute-force enumeration for small groups.
    pub fn compute(group: &FiniteGroup) -> Self {
        let n = group.order();
        if n == 0 {
            return Self { automorphisms: Vec::new(), order: 0, inner_indices: Vec::new() };
        }
        // For small groups, enumerate all bijections that are homomorphisms
        let mut automorphisms = Vec::new();
        if n <= 12 {
            // Try all permutations of elements
            let mut perm: Vec<usize> = (0..n).collect();
            loop {
                let aut = Automorphism::new(perm.clone());
                if aut.verify_homomorphism(group) {
                    automorphisms.push(aut);
                }
                if !next_permutation(&mut perm) { break; }
            }
        } else {
            // For larger groups, use inner automorphisms as a starting point
            let inners = InnerAutomorphism::all_inner_automorphisms(group);
            for inner in &inners {
                automorphisms.push(inner.automorphism.clone());
            }
        }
        // Find inner automorphisms
        let inner_auts: Vec<Automorphism> = InnerAutomorphism::all_inner_automorphisms(group)
            .into_iter().map(|i| i.automorphism).collect();
        let inner_set: HashSet<Vec<usize>> = inner_auts.iter()
            .map(|a| a.mapping.clone()).collect();
        let inner_indices: Vec<usize> = automorphisms.iter().enumerate()
            .filter(|(_, a)| inner_set.contains(&a.mapping))
            .map(|(i, _)| i)
            .collect();
        let order = automorphisms.len();
        Self { automorphisms, order, inner_indices }
    }

    /// Get all automorphisms.
    pub fn generators(&self) -> &[Automorphism] { &self.automorphisms }

    /// Get the order.
    pub fn order(&self) -> usize { self.order }

    /// Check if an automorphism is in the group.
    pub fn is_member(&self, aut: &Automorphism) -> bool {
        self.automorphisms.iter().any(|a| a.mapping == aut.mapping)
    }

    /// Get the inner subgroup.
    pub fn inner_subgroup(&self) -> Vec<&Automorphism> {
        self.inner_indices.iter().map(|&i| &self.automorphisms[i]).collect()
    }

    /// Compute the outer quotient.
    pub fn outer_quotient(&self) -> OuterAutomorphismGroup {
        let inner_auts: Vec<Automorphism> = self.inner_indices.iter()
            .map(|&i| self.automorphisms[i].clone())
            .collect();
        OuterAutomorphismGroup::compute(&self.automorphisms, &inner_auts)
    }

    /// Check if the group is complete (Out(G) is trivial and Z(G) is trivial).
    pub fn is_complete(&self, group: &FiniteGroup) -> bool {
        let outer = self.outer_quotient();
        let center = group.center();
        outer.order == 1 && center.len() == 1
    }
}

/// Compute the next permutation in lexicographic order.
fn next_permutation(perm: &mut Vec<usize>) -> bool {
    let n = perm.len();
    if n <= 1 { return false; }
    let mut i = n - 1;
    while i > 0 && perm[i - 1] >= perm[i] { i -= 1; }
    if i == 0 { return false; }
    let mut j = n - 1;
    while perm[j] <= perm[i - 1] { j -= 1; }
    perm.swap(i - 1, j);
    perm[i..].reverse();
    true
}

